    private void formWindowOpened(java.awt.event.WindowEvent evt) {//GEN-FIRST:event_formWindowOpened
        // TODO add your handling code here:
        //Fill all the tables for MIABIS attributes in all the tabs
        
        //select Sample Collexction tab
        jTabbedPane1.setSelectedIndex(0);
        String fileToParse;
        String current = System.getProperty("user.dir");
        fileToParse = current+"/MIABIS-Mapper.csv";
        BufferedReader fileReader = null;
        String DELIMITER = ";";
        DefaultTableModel myModelSC = (DefaultTableModel)TableSCMIABIS.getModel();
        DefaultTableModel myModelST = (DefaultTableModel)TableSTMIABIS.getModel();
        DefaultTableModel myModelSA = (DefaultTableModel)TableSAMIABIS.getModel();
        DefaultTableModel myModelCI = (DefaultTableModel)TableCIMIABIS.getModel();
        DefaultTableModel myModelLV = (DefaultTableModel)TableLVMIABIS.getModel();
        String[] term = null;
        try
        {
            String line = "";
            //Create the file reader
            fileReader = new BufferedReader(new FileReader(fileToParse));
             
            //Read the file line by line
            while ((line = fileReader.readLine()) != null)
            {
                
               term = line.split(DELIMITER);
                Object[] RowData = {term[0],term[1],term[2]};
                if ("Sample Collection".equals(term[0])){
                    myModelSC.addRow(RowData);
                }
                if ("Study".equals(term[0])){
                    myModelST.addRow(RowData);
                }
                if ("Sample".equals(term[0])){
                    myModelSA.addRow(RowData);
                }
                if ("Contact Information".equals(term[0])){
                    myModelCI.addRow(RowData);
                }
                if ("List Values".equals(term[0])){
                    myModelLV.addRow(RowData);
                }
           
            }
           
        }
        catch (Exception e) {
            e.printStackTrace();
        }
        finally
        {
            try {
                fileReader.close();
            } catch (IOException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_formWindowOpened

