    public void readFileInTable(JTable TheTable, JLabel TheLabel, JComboBox TheComboBox){
        //System.out.println("readFileInTable");
    
        
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setCurrentDirectory(new File(System.getProperty("user.home")));
        int result = fileChooser.showOpenDialog(this);
        if (result == JFileChooser.APPROVE_OPTION) {
            File selectedFile = fileChooser.getSelectedFile();
            TheLabel.setText(selectedFile.getAbsolutePath());
        }
        // parse file and fill table
        String csvFile = TheLabel.getText();
        
		BufferedReader br = null;
		String line = "";
		String cvsSplitBy = (String) TheComboBox.getSelectedItem();
        
		if((cvsSplitBy =="Tab")){
            cvsSplitBy="\t";
        }
		
        String[] term = null;
        int row = 0;
        
        DefaultTableModel myModel = (DefaultTableModel) TheTable.getModel();
        //Reset the table
        while(myModel.getRowCount()>0){
             myModel.removeRow(0);
        }
        
        // read file
        try {

        	br = new BufferedReader(new FileReader(csvFile));
        	line = br.readLine();
		
			// use comma as separator
			term = line.split(cvsSplitBy);
	
			//loop map
	        
	        int columnNumber = 1;
			for (String str : term) {
				//fill jTable3 - mapper talbe
	            System.out.println(str);
	            Object[] RowData = {columnNumber,str};
	                    
	            myModel.addRow(RowData);
	            columnNumber++;
			}
		} catch (FileNotFoundException e) {
		e.printStackTrace();
		} catch (IOException e) {
			e.printStackTrace();
		} 
        finally {
        	if (br != null) {
        		try {
        			br.close();
        		}catch (IOException e) {
        			e.printStackTrace();
        		}
        	}
        }
    }
   
    
